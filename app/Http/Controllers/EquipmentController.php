    public function store(Request $request)
    {
         $this->validate($request,[
             'ename' => 'required',
             'ecode' => 'required',
             'eclass' => 'required',
         ]);
         $emps = new Equipment;

         $emps->ename = $request->input('ename');
         $emps->ecode = $request->input('ecode');
         $emps->eclass = $request->input('eclass');
        

         $emps->save();
         return redirect('/Equipment')->with('success', 'Data Saved');
    }

